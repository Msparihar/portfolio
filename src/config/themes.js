/**
 * Apply a theme to the desktop canvas element.
 * Merges base vars with the theme's delta vars, then sets them
 * as inline CSS custom properties on the .desktop-canvas element.
 *
 * @param {HTMLElement} canvasEl - The .desktop-canvas DOM element
 * @param {string} themeId - The theme id to apply (e.g. 'neutral', 'emerald')
 */
export function applyTheme(canvasEl, themeId) {
  const theme = THEMES.find((t) => t.id === themeId) ?? THEMES[0];
  const merged = { ...BASE_THEME_VARS, ...theme.vars };
  Object.entries(merged).forEach(([prop, value]) => {
    canvasEl.style.setProperty(prop, value);
  });
}
